    public ArrayList<Integer> searchRange(final List<Integer> a, int b) {
        ArrayList<Integer> res = new ArrayList<>();
        ArrayList<Integer> list = new ArrayList<Integer>(a);
        if (list.size() == 1 && list.get(0) == b) {
            res.add(0);
            res.add(0);
            return res;
        }
        int min = 0;
        int max = list.size();
        while (min+1 < max) {
            int mid = (max + min) / 2;
            int c = list.get(mid);
            for(int i=min; i<max; i++) {
                System.out.print(a.get(i)+",");
            }
            System.out.println("("+min+","+max+") mid = "+mid+", val="+c);
            if (c == b) {
                // check for range
                int left = mid;
                for (int i=mid; i>=0; i--) {
                    if (list.get(i) == b) {
                        left = i;
                    } else {
                        break;
                    }
                }

                int right = mid;
                for (int i=mid; i<list.size(); i++) {
                    if (list.get(i) == b) {
                        right = i;
                    } else {
                        break;
                    }
                }
                res.add(left);
                res.add(right);
                return res;
            }
            if (c > b) {
                max = mid;
            } else {
                min = mid;
            }
        }
        res.add(-1);
        res.add(-1);
        return res;
    }

